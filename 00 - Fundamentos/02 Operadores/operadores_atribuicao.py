# Instanciando uma variável
saldo = 500
# Imprimi o saldo
print(saldo)
# Muda o valor do saldo
saldo = 200
# Imprimi o saldo
print(saldo)
# Adiciono mais 10 ao saldo
saldo += 10
# Imprimi o saldo
print(saldo)
# Retira 5 ao saldo
saldo -= 5
# Imprimi o saldo
print(saldo)
# Dividi o saldo em dois, mostrando em ponto flutuante
saldo //= 2
# Imprimi o saldo
print(saldo)
# Dividi o saldo em dois, mostrando inteiro
saldo /= 2
# Imprimi o saldo
print(saldo)
# Multiplica o saldo por 10
saldo *= 10
# Imprimi o saldo
print(saldo)
# Mostra o resto da divisão
saldo %= 4
# Imprimi o saldo
print(saldo)
# Multiplica expontecialmente
saldo **= 2
# Imprimi o saldo
print(saldo)